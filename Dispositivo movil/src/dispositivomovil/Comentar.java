    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
            ControladorDispositivo c = ControladorDispositivo.getInstance();
            DateFormat dateFormat = new SimpleDateFormat("dd/MM HH:mm");
            Date date = new Date();
            String fecha = dateFormat.format(date);
            String idRespuesta = c.getIdComentario();
            try{
                c.ingresarComentario("Sim칩n", this.Comentario.getText(), fecha, (Integer)puntos.getValue(), idRespuesta);
                JOptionPane.showMessageDialog(this, "Se ha ingresado su comentario!");
            } catch (WebServiceException e){
                JOptionPane.showMessageDialog(this, "Su comentario ingresar치\ncuando recupere la conexi칩n");
            }
            MostrarComentarios vistaComentarios = new MostrarComentarios();
            JDesktopPane Esc = this.getDesktopPane();
            Esc.add(vistaComentarios);
            vistaComentarios.ocultarBarraTitulo();
            vistaComentarios.show();
            vistaComentarios.setLocation(39, 126);
            this.dispose();
    }//GEN-LAST:event_AceptarActionPerformed


