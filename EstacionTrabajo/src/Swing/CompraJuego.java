    private void confirmar(){
        String nick = nickCliente.getText();
        if(icj.perteneceUsuario(nick)){
            DateFormat dateFormat = new SimpleDateFormat("dd/MM HH:mm");
            Date date = new Date();
            String fecha = dateFormat.format(date);
            try{
                DataCompra dc = icj.ingresarDatosCliente(nick, fecha);
                int confirmado = JOptionPane.showConfirmDialog(null, "¿Confirmar compra del cliente " + nick + " en la fecha " + fecha + "?", "CONFIRMAR COMPRA", 2);

                if (JOptionPane.OK_OPTION == confirmado){
                    icj.registrarCompra();
                    JOptionPane.showMessageDialog(null, "¡Compra realizada con éxito!");
                    this.dispose();
                }else{
                    icj.terminar();
                    this.dispose();
                }
            }
            catch(ExcAccesoUsuario e){
                JOptionPane.showMessageDialog(null, e.getMessage());
                nickCliente.setText("");
            }


        }else{
            JOptionPane.showMessageDialog(null, "El cliente ingresado no es correcto");
            nickCliente.setText("");
        }

    }

