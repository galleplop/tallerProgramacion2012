    private void tablaJuegosMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaJuegosMouseMoved
        int F = tablaJuegos.rowAtPoint(evt.getPoint());
        String nomJuego = (String)tablaJuegos.getValueAt(F, 0);
        DataInfoJuego data = icj.seleccionarJuego(nomJuego);
        desc.setText(data.getDescripcion());
    }//GEN-LAST:event_tablaJuegosMouseMoved

    private void tablaJuegosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaJuegosMouseClicked
        int F = tablaJuegos.rowAtPoint(evt.getPoint());
        String nomJuego = (String)tablaJuegos.getValueAt(F, 0);
        icj.ingresarNombreJuego(nomJuego);
        MouseMotionListener[] m = tablaJuegos.getMouseMotionListeners();
        if (m.length>1){
            tablaJuegos.removeMouseMotionListener(m[1]);
        }
        DataInfoJuego data = icj.seleccionarJuego(nomJuego);
        desc.setText(data.getDescripcion());
        nickLabel.setEnabled(true);
        botonCompra.setEnabled(true);
        nickCliente.setEnabled(true);
    }//GEN-LAST:event_tablaJuegosMouseClicked

